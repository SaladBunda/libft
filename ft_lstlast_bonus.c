#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*ptr;

	if (!lst)
		return (NULL);
	while (lst != NULL)
	{
		ptr = lst;
		lst = lst->next;
	}
	return (ptr);
}
